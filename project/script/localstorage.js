sushi()
function sushi(){
    const sub = document.querySelector('#subsushi');
sub.addEventListener('click',()=>{
    const today = new Date();

    const day = today.getDay()+1;
    const month = today.getMonth()+1;
    const year = today.getFullYear();
    const time = today.getHours() + ":" + today.getMinutes() + ":" + today.getSeconds();
    const Datee= `${day}/${month}/${year} : ${time}`;


    const name = document.querySelector('#sushiname').value;
    const num = document.querySelector('#sushiqty').value;
    const add = document.querySelector('#sushiadd').value;
    const phone = document.querySelector('#sushiphone').value;
    const info = `DATE-TIME:${Datee}/ NAME:${name}/ QTY:${num}/ ADDRESS:${add}/ TEL:${phone}`;
    localStorage.setItem("order",info);
})
}



